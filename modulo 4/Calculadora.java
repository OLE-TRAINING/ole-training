package ExerciciosChapter4;

public class Calculadora {

    public double soma (double elem01, double elem02){
        double result = elem01 + elem02;
        return result;
    }

    public double divisao (double elem01, double elem02){
        double result = elem01 / elem02;
        return result;
    }

    public double multiplicacao (double elem01, double elem02){
        double result = elem01 * elem02;
        return result;
    }

    public double subtracao (double elem01, double elem02){
        double result = elem01 - elem02;
        return result;
    }
}